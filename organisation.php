						<h1>Organisation du Campus Numérique</h1>
						<p>Le Campus Numérique et la Cellule Innovation Pédagogique de l'UPEM travaillent de concert afin de développer des modes pédagogiques innovants mais aussi afin de permettre aux enseignants de pouvoir enseigner, communiquer et effectuer leurs recherches scientifiques avec le numérique.</p>

						<p>Le Campus Numérique est divisé en deux services :</p>
						<ul>
							<li>Le services Technologies de l'Information et de la Communication (TIC) qui a pour missions la gestion des plateformes numériques pédagogiques et de communication, la médiatisation de contenus pédagogiques, la formation des utilisateurs aux outils et aux nouveaux usages du numérique, l'intégration et la promotion des innovations pédagogiques dans la formation.
								Le service audiovisuel qui a pour missions la centralisation de l'achat public en matière d'audiovisuel, la production de vidéo pour la pédagogie et pour la recherche, la gestion et le développement des infrastructures audiovisuelles (salles et amphis), la visioconférence mais aussi la médiatisation de contenus scientifiques avec le numérique.

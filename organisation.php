							<li>La Cellule Innovation Pédagogique, rattachée à la Vice-Présidence Enseignements et Professionnalisation, développe de nouveaux modes pédagogiques en lien avec les enseignants et enseignants-chercheurs. Elle effectue la veille pédagogique nécessaire au développement de la pédagogie dans l'établissement.</li>
						</ul>

						<p>Les deux services s'appuient mutuellement afin de pouvoir transformer les modes pédagogiques et l'organisation. Ils organisent annuellement les "Rencontres de l'innovation pédagogique", véritable moment d'échanges sur la pédagogie et de retours d'expériences.</p>

						<p>Les deux services comptabilisent en tout 20 agents.</p>

						<p>Avec la collaboration de l'IDEFI IDEA, porté par la Comue Université Paris-Est, véritable catalyseur des innovations pédagogiques et numériques, le Campus Numérique et la Cellule Innovation Pédagogique bénéficient de l'expérience d'experts internationaux, d'une Cellule de Pilotage (Cellule IDEA), ainsi que de moyens afin de développer les innovations pédagogiques et numériques.</p>
